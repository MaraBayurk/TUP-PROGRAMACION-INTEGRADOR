# Retorna la longitud de un nodo espec√≠fico en el √°rbol.
def longitud_camino(nodo, destino, nivel=0):
    if type(nodo) == str:
        return nivel if nodo == destino else -1
    pregunta, opciones = nodo
    for opcion, hoja in opciones:
        res = longitud_camino(hoja, destino, nivel + 1)
        if res != -1:
            return res
    return -1

#Retorna la profundidad m√°xima del √°rbol.
def altura_max(nodo):
    if type(nodo) == str:
        return 1
    _, opciones = nodo
    alt = 0
    for opcion, hoja in opciones:
        sub_alt = altura_max(hoja)
        if sub_alt > alt:
            alt = sub_alt
    return alt + 1

# Imprime el √°rbol de decisiones de forma estructurada
def imprimir_arbol(nodo, nivel=0):
    if type(nodo) == str:
        print('  ' * nivel + '- ' + nodo)
    else:
        pregunta, opciones = nodo
        print('  ' * nivel + pregunta)
        for opcion, subnodo in opciones:
            print('  ' * (nivel + 1) + f'[{opcion}]')
            imprimir_arbol(subnodo, nivel + 2)

