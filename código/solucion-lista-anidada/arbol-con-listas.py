def jugar_razas(nodo):
    while True:
        pregunta, opciones = nodo
        respuesta = input(pregunta + " Responde con si o con no: ").strip().capitalize()
        for opcion, hoja in opciones:
            if respuesta == opcion:
                if type(hoja) == str:  # validaci√≥n directa del tipo
                    return hoja
